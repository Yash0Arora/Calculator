sign = input("What Do You Want Me To Do ? (+ , - , * , /) ")
if sign == "+" :

    first_number = input("First Number =  " )
    second_number = input("Second Number =  ")
    sum = float(first_number) + float(second_number)
    print("Sum = " + str(sum))

if sign == "-" : 
    
    first_number = input("First Number =  " )
    second_number = input("Second Number =  ")
    sum = float(first_number) - float(second_number)
    print("Sum = " + str(sum))

if sign == "*" : 

    
    first_number = input("First Number =  " )
    second_number = input("Second Number =  ")
    sum = float(first_number) * float(second_number)
    print("Sum = " + str(sum))

if sign == "/" :

    first_number = input("First Number =  " )
    second_number = input("Second Number =  ")
    sum = float(first_number) / float(second_number)
    print("Sum = " + str(sum))



